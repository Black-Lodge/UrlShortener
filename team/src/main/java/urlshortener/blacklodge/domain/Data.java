package urlshortener.blacklodge.domain;

/**
 * Class that represents some data for the Imgflip API
 */
public class Data {
    private String url;
    private String page_url;
    /** Get url
     * @return the url
     */
    public String getUrl() {
        return url;
    }
    /** Set url
     * @param url the url to set
     */
    public void setUrl(String url) {
        this.url = url;
    }
    /** Get page_url
     * @return the page_url
     */
    public String getPage_url() {
        return page_url;
    }
    /** Set page_url
     * @param page_url the page_url to set
     */
    public void setPage_url(String page_url) {
        this.page_url = page_url;
    }

    
    
}
